-- # 테이블 생성
CREATE TABLE users (
  user_id INT PRIMARY KEY,
  username VARCHAR(255) NOT NULL,
  user_birth_date DATE NOT NULL,
  gender ENUM('M', 'F', 'O') NOT NULL,
  created_at TIMESTAMP,
  status TINYINT,
  email VARCHAR(255) NOT NULL UNIQUE,
  password VARCHAR(255) NOT NULL,
  profile_image TEXT
);

CREATE TABLE category (
  category_id INT PRIMARY KEY,
  category_name VARCHAR(255) NOT NULL,
  type ENUM('expense', 'income') NOT NULL
);

CREATE TABLE usages (
  usage_id INT PRIMARY KEY,
  user_id INT NOT NULL,
  usage_title VARCHAR(255) NOT NULL,
  usage_memo TEXT,
  usage_price INT NOT NULL,
  usage_date DATETIME NOT NULL,
  created_at TIMESTAMP,
  usage_category INT NOT NULL,
  FOREIGN KEY (user_id) REFERENCES users(user_id),
  FOREIGN KEY (usage_category) REFERENCES category(category_id) ON DELETE CASCADE
);

CREATE TABLE incomes (
  income_id INT PRIMARY KEY,
  user_id INT NOT NULL,
  income_title VARCHAR(255) NOT NULL,
  income_memo TEXT,
  income_price INT NOT NULL,
  income_date DATETIME NOT NULL,
  created_at TIMESTAMP,
  income_category INT NOT NULL,
  FOREIGN KEY (user_id) REFERENCES users(user_id),
  FOREIGN KEY (income_category) REFERENCES category(category_id)
);


CREATE TABLE budgets (
  budget_id INT PRIMARY KEY,
  user_id INT NOT NULL,
  budget_amount INT NOT NULL,
  start_date DATE NOT NULL,
  end_date DATE NOT NULL,
  created_at TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES users(user_id)
);

CREATE TABLE user_categories (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  category_id INT NOT NULL,
  FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE CASCADE,
  FOREIGN KEY (category_id) REFERENCES category(category_id) ON DELETE CASCADE,
  UNIQUE (user_id, category_id)
);

